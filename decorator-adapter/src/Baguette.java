/**
* Interfaz Baguette
* @author: Diego Martinez Calzada
*/

public interface Baguette {
  
  /**
  * Metodo que regresa el precio del pan
  * @return double -- el precio del pan
  */
  public double getPrecioPan();
  
  /**
  * Metodo que regresa la descripcion de la baguette
  * @return String -- descripcion de la baguette
  */
  public String getDescripcion();
  
  /**
  * Metodo que regresa la cantidad de pollo
  * @return int -- cantidad de pollo
  */
  public int getCantidadPollo();
  
  /**
  * Metodo que regresa la cantidad de pepperoni
  * @return int -- cantidad de pepperoni
  */
  public int getCantidadPepperoni();
  
  /**
  * Metodo que regresa la cantidad de jamon
  * @return int -- cantidad de jamon
  */
  public int getCantidadJamon();
  
  /**
  * Metodo que regresa la cantidad de lechuga
  * @return int -- cantidad de lechuga
  */
  public int getCantidadLechuga();
  
  /**
  * Metodo que regresa la cantidad de jitomate
  * @return int -- cantidad de jitomate
  */
  public int getCantidadJitomate();
  
  /**
  * Metodo que regresa la cantidad de cebolla
  * @return int -- cantidad de cebolla
  */
  public int getCantidadCebolla();
  
  /**
  * Metodo que regresa la cantidad de mostaza
  * @return int -- cantidad de mostaza
  */
  public int getCantidadMostaza();
  
  /**
  * Metodo que regresa la cantidad de catsup
  * @return int -- cantidad de catsup
  */
  public int getCantidadCatsup();
  
  /**
  * Metodo que regresa la cantidad de mayonesa
  * @return int -- cantidad de mayonesa
  */
  public int getCantidadMayonesa();

  /**
  * Metodo que regresa el precio de la baguette
  * @return double -- el precio de la baguette
  */
  public double getPrecio();
}